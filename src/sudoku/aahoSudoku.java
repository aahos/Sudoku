int sudokumatrix[][]= new int[9][9];
int top[]=new int[81],count;
int cheatbit=0;
int stack[][] = new int[81][9];
void push(int s,int val)
{
    stack[s][++top[s]]=val;
}
int pop(int s)
{
    int aaho;
    aaho= stack[s][top[s]--];
    return aaho;
}

    boolean sudoku(int x,int y)
{
    if(x<9&&y<9)
    {
        if(sudokumatrix[x][y]==0)
        {
            all_possible_entries(x,y);
            while(top[x*9+y]>-1)
            {
                sudokumatrix[x][y]=pop(x*9+y);
                if(sudoku(x+(y+1)/9,(y+1)%9))
                    return true;
            }
            sudokumatrix[x][y]=0;
            return false;
        }
        return sudoku(x+(y+1)/9,(y+1)%9);
    }
    count++;
    return true;
}
void all_possible_entries(int x,int y)
{
    int arr[]= new int[10];
    int i,j,bx,by;
    for(j=0;j<9;j++)
        arr[sudokumatrix[x][j]]=1;
    for(i=0;i<9;i++)
        arr[sudokumatrix[i][y]]=1;
    bx=x/3;
    by=y/3;
    for(i=bx*3;i<(bx+1)*3;i++)
        for(j=by*3;j<(by+1)*3;j++)
        arr[sudokumatrix[i][j]] = 1;
    for(i=1;i<10;i++)
        if(arr[i]==0)
        push(x*9+y,i);
}
void check()
{ 
    int filled_boxes =0;
    for(int i=0;i<9;i++)
        for(int j=0;j<9;j++)
            if(sudokumatrix[i][j]!=0)
                filled_boxes++;
    if(filled_boxes==81&&cheatbit==0)
        chkbtn.setEnabled(true);
    else chkbtn.setEnabled(false);
}
