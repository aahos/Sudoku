        int falg =0;
        int sum =0;
        for(int i=0;i<9&&falg==0;i++)
        {
            for(int j =0;j<9;j++)
                sum = sum +sudokumatrix[i][j];
            if(sum!=45)
                falg=1;
        }
        if(falg==0)
        {
            for(int i=0;i<9&&falg==0;i++)
            {
                for(int j =0;j<9;j++)
                    sum = sum +sudokumatrix[j][i];
                if(sum!=45)
                    falg=1;
            }
        }
        if(falg==0)
            JOptionPane.showMessageDialog(null,"The Solution is Correct!");
        else JOptionPane.showMessageDialog(null,"The Solution is InCorrect!");
